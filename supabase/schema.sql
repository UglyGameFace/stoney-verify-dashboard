-- =========================================================
-- verification_tokens
-- =========================================================
CREATE TABLE IF NOT EXISTS public.verification_tokens (
  token text NOT NULL CHECK (length(token) >= 6),
  webhook_url text NOT NULL CHECK (length(webhook_url) > 0),
  expires_at timestamp with time zone NOT NULL,
  used boolean NOT NULL DEFAULT false,
  created_at timestamp with time zone NOT NULL DEFAULT now(),
  guild_id text,
  channel_id text,
  requester_id text,
  decision text DEFAULT 'PENDING'::text,
  decided_by text,
  decided_at timestamp with time zone,
  user_id text,
  submitted boolean DEFAULT false,
  submitted_at timestamp with time zone,
  ai_status text,
  owner_display_name text,
  owner_username text,
  owner_tag text,
  approved_user_id text,
  updated_at timestamp with time zone,
  status text DEFAULT 'pending'::text
    CHECK (
      status = ANY (
        ARRAY[
          'pending'::text,
          'submitted'::text,
          'approved'::text,
          'denied'::text,
          'resubmit'::text,
          'used'::text,
          'expired'::text
        ]
      )
    ),
  requester_display_name text,
  requester_username text,
  requester_avatar_url text,
  requester_role_ids text[] DEFAULT '{}'::text[],
  requester_role_names text[] DEFAULT '{}'::text[],
  expected_role_state text DEFAULT 'unverified_only'::text
    CHECK (
      expected_role_state = ANY (
        ARRAY[
          'unverified_only'::text,
          'verified_ok'::text,
          'staff_ok'::text,
          'unknown'::text
        ]
      )
    ),
  actual_role_state text DEFAULT 'unknown'::text
    CHECK (
      actual_role_state = ANY (
        ARRAY[
          'unverified_only'::text,
          'verified_ok'::text,
          'verified_conflict'::text,
          'staff_ok'::text,
          'staff_conflict'::text,
          'missing_unverified'::text,
          'missing_verified_role'::text,
          'booster_only'::text,
          'left_guild'::text,
          'unknown'::text,
          'bot_ok'::text
        ]
      )
    ),
  role_sync_ok boolean DEFAULT false,
  role_sync_reason text,
  decided_by_display_name text,
  decided_by_username text,
  decided_by_avatar_url text,
  CONSTRAINT verification_tokens_pkey PRIMARY KEY (token)
);

